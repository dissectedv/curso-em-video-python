# Operadores aritmeticos
n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))

s = n1 + n2
su = n1 - n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
r = n1 % n2

print(f'\033[35mA soma vale: {s},\nA subtracao vale: {su}\nO produto eh: {m}\nA divisao eh: {d}')
print(f'\033[35mA divisao inteira vale: {di},\nA potencia eh: {e}\nO resto: {r}')

